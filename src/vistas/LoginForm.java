    public void validar(){
        String us=txtUsuario.getText();
        String cont=txtContrasena.getText();
        if (txtUsuario.getText().equals("") || txtContrasena.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "DEBE DE INGRESAR LOS DATOS QUE SE PIDEN");
            txtUsuario.requestFocus();
        }
        else{
            em=mdao.ValidarMaestro(us,cont);
            if(em.getUsuario() != null && em.getContrasena() != null){
                Principal p=new Principal();
                p.setVisible(true);
                dispose();
            }
            else{
                JOptionPane.showMessageDialog(this,"DATOS INCORRECTOS");
                txtUsuario.requestFocus();
            }
        }
    }
    
